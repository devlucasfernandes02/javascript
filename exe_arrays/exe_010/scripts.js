const calculadora = {
    somar: function(a,b) {
        return a + b;
    },
    subtrair: function(a,b) {
        return a - b;
    },
    multiplicação: function(a,b) {
        return a * b;
    },
    divisão: function(a,b) {
        return a / b;
    } 
    
    
}

